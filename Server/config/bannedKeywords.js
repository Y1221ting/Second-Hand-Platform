// 违禁词检查（公共模块，供所有 controller 和 route 使用）
function checkBanned(text) {
  if (!text) return null;
  const lower = text.toLowerCase();
  return bannedKeywords.find(kw => lower.includes(kw.toLowerCase()));
}

