for idx, new_texts in content.items():
    slide = prs.slides[idx]
    shapes = [s for s in slide.shapes if s.has_text_frame]
    
    for si, shape in enumerate(shapes):
        if si >= len(new_texts):
            break
        tf = shape.text_frame
        new_txt = new_texts[si]
        
        # 安全替换：保留第一个段落的第一个run的字体样式
        for pi, para in enumerate(tf.paragraphs):
            if pi == 0:
                runs = para.runs
                if runs:
                    # 保留第一个run的字体，改其文字
                    font = runs[0].font
                    runs[0].text = new_txt
                    # 清掉后面的run
                    for r in runs[1:]:
                        r.text = ""
                else:
                    # 没有run就加一个
                    run = para.add_run()
                    run.text = new_txt
            else:
                # 清掉多余段落
                for r in para.runs:
                    r.text = ""

